class BitWiseOperator
{
	public static void main(String[] args) {
		
		 System.out.println(10&2); // Bitwise AND (&) Operator;
		 System.out.println(10|2); // Bitwise Or (|) Operator;
		 System.out.println(10^2);	// Bitwise XOR (^) Operator;
		  System.out.println(~10);	// Bitwise tilde  (~) Operator;
	}
}
